package com.example.mybatis0603.entity;

public class Name {
    private int id;
    private String name;

    public Name(String name) {
        this.id = id;
        this.name = this.name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
